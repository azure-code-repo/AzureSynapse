CREATE VIEW [V_EDAA_DW].[FCT_DLY_STR_ITMSKU_INV_ADJ_DTL]
AS SELECT [PROD_HIST_SK]
      ,[GEO_HIST_SK]
	  ,[DT_SK]
	  ,[STR_ID]
      ,[ITM_SKU]
      ,FCT.[INV_ADJ_SK]
	  ,DIM.[ADJ_TYP]
	  ,DIM.[TXN_TYP]
      ,SUM([INV_ADJSTD_QTY]) AS [INV_ADJSTD_QTY]
      ,CAST(SUM([INV_ADJSTD_AMT]) AS DECIMAL(18,2)) AS INV_ADJSTD_AMT
FROM [EDAA_DW].[FCT_DLY_STR_ITMSKU_INV_ADJ] FCT WITH(NOLOCK) JOIN [EDAA_DW].[DIM_INV_ADJ] DIM WITH(NOLOCK)
ON FCT.[INV_ADJ_SK]=DIM.[INV_ADJ_SK]
GROUP BY [PROD_HIST_SK]
      ,[GEO_HIST_SK]
      ,[DT_SK]
      ,[STR_ID]
	  ,[ITM_SKU]
      ,FCT.[INV_ADJ_SK]
	  ,DIM.[ADJ_TYP]
	  ,DIM.[TXN_TYP];
